void main() {
  num a = 3;
  num b = 15;
  print('a + b = ${a + b}'); // 18
  print('a - b = ${a - b}'); // -12
  print('-a = ${-a}'); // -3
  print('a * b = ${a * b}'); // 45
  print('a / b = ${a / b}'); // 0.2
  print('a ~/ b = ${a ~/ b}'); // 0
  print('a % b = ${a % b}'); // 3
  print('a = $a'); // 3
  print('++a = ${++a}'); // 4
  print('a = $a'); // 4
  print('a++ = ${a++}'); // 4
  print('a = $a'); // 5
  print('b = $b'); // 15
  print('--b = ${--b}'); // 14
  print('b = $b'); // 14
  print('b-- = ${b--}'); // 14
  print('b = $b'); // 13
}
