void main() {
  num a = 3;
  num b = 15;
  print('a = $a');
  print('b = $b');
  print('-------------')
  print('a == b -> ${a == b}'); // false
  print('a != b -> ${a != b}'); // true
  print('a > b -> ${a > b}'); // false
  print('a < b -> ${a < b}'); // true
  print('a >= b -> ${a >= b}'); // false
  print('a <= b -> ${a <= b}'); // true
}
